#!/usr/bin/env python3
"""Simple concatenation module"""


def concat(str1: str, str2: str) -> str:
    """
    A simple string concatenator function

    Args:
        str1[str]: the first string
        str2[str]: the second string
    Return:
        [str]: a concatenation of str1 and str2
    """
    return str1 + str2
